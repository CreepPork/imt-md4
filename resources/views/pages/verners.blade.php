        immersion.<br><br>

        The gameplay mechanics are fairly simple, with the player controlling Arbaaz as he jumps, climbs, and fights his
        way through the levels. The game also features a stealth system, which allows players to sneak past enemies
        undetected and take them out with silent assassinations. The combat is relatively basic, with a limited set of
        moves and combos, but it is still satisfying to take down enemies with Arbaaz's deadly skills.<br><br>

